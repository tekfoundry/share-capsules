import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

import Ajv2020 from 'ajv/dist/2020.js';
import standaloneCode from 'ajv/dist/standalone/index.js';
import addFormats from 'ajv-formats';
import { format } from 'prettier';

import manifestSchema from '../packages/capsule-core/src/schema/capsule-manifest-v1.schema.json' with { type: 'json' };
import policySchema from '../packages/capsule-core/src/schema/ctx-policy-v1.schema.json' with { type: 'json' };
import contractsSchema from '../packages/ctx-client/src/schema/ctx-contracts-v1.schema.json' with { type: 'json' };

const root = resolve(import.meta.dirname, '..');

await generate(
    'packages/capsule-core/src/generated/schema-validators.js',
    [policySchema, manifestSchema],
    {
        validatePolicySchema: policySchema.$id,
        validateManifestSchema: manifestSchema.$id,
    },
);

const contractNames = [
    'providerMetadata',
    'brokerMetadata',
    'ticketHeader',
    'ticketClaims',
    'dpopHeader',
    'dpopClaims',
    'ticketProofHeader',
    'ticketProofClaims',
    'ticketSigningJwks',
    'hpkeEnvelope',
    'error',
];

await generate(
    'packages/ctx-client/src/generated/schema-validators.js',
    [contractsSchema],
    Object.fromEntries(
        contractNames.map((name) => [`${name}Validator`, `${contractsSchema.$id}#/$defs/${name}`]),
    ),
);

async function generate(relativePath, schemas, exports) {
    const ajv = new Ajv2020({
        allErrors: true,
        code: { esm: true, source: true },
        strict: true,
    });
    addFormats(ajv);
    for (const schema of schemas) ajv.addSchema(schema);

    const output = resolve(root, relativePath);
    await mkdir(dirname(output), { recursive: true });
    const imports = [
        "import formatsModule from 'ajv-formats/dist/formats.js';",
        "import ucs2lengthModule from 'ajv/dist/runtime/ucs2length.js';",
        "import equalModule from 'ajv/dist/runtime/equal.js';",
        'const unwrapDefault = (module) => { let value = module; while (value !== null && typeof value === "object" && "default" in value) value = value.default; return value; };',
        'const formats = unwrapDefault(formatsModule);',
        'const ucs2length = unwrapDefault(ucs2lengthModule);',
        'const equal = unwrapDefault(equalModule);',
    ].join('\n');
    const standalone = standaloneCode(ajv, exports)
        .replaceAll('require("ajv-formats/dist/formats").fullFormats', 'formats.fullFormats')
        .replaceAll('require("ajv/dist/runtime/ucs2length").default', 'ucs2length')
        .replaceAll('require("ajv/dist/runtime/equal").default', 'equal');
    const source = `/* eslint-disable */\n// Generated by scripts/generate-schema-validators.mjs. Do not edit.\n${imports}\n${standalone}`;
    await writeFile(
        output,
        await format(source, {
            filepath: output,
            printWidth: 100,
            singleQuote: true,
            tabWidth: 4,
            trailingComma: 'all',
        }),
    );
}
